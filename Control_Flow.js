/*
- By default JS code is executed from top to bottom, line by line
however we can change this with Control Flow
-Control Flow allows our program to make decisions about what code
is executed and when

1. Conditional Statements - are basically checks to see if a certain
condition is either true or false. If the condition is true then run code A,
if it's false then run code B (if statements, switch, ternary operator)

2. Loops - allows us to perform repetitive tasks with less code. For example
using loops we can iterate over an array and perform some sort of action on
each piece of data in the array, or we can use a loop to compile a list of 
all the items (for loop, do/while loop, for..in, for..of)

*/